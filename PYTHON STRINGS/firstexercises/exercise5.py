#Python relational operators
"""All the comparison (relational) operators i.e (<,><=,>=,==,!=,<>) are also applicable for strings.The Strongs are
compared based on the ASCII value or unicode (i.e),dictionary order."""
#Example :
print("Moshek" == "Moshek")
print("Shaju ones" <= "shaju Jones")
print("M"<"J")