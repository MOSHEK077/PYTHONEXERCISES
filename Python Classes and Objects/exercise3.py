class Myclass:
    def __init__(self,name,age):
        self.name = name
        self.age = age
obj1 = Myclass("Shaju",21)
obj2 = Myclass("Jones",22)
obj3 = Myclass("Ramesh",26)
obj4 = Myclass("Jackson",42)

print("Name:",obj1.name,"|","Age:",obj1.age)
print("Name:",obj2.name,"|","Age:",obj2.age)
print("Name:",obj3.name,"|","Age:",obj3.age)
print("Name:",obj4.name,"|","Age:",obj4.age) 